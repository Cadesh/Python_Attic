from __future__ import unicode_literals
import youtube_dl

#--------------------------------------------------------------------
# LOAD THE FILE DOWNLOAD THE SOUNDS
#--------------------------------------------------------------------
def get_list(fileName):
    import numpy as np
    return np.genfromtxt(fileName,dtype='str')
#--------------------------------------------------------------------

#--------------------------------------------------------------------
# YOUTUBE PARAMETERS
#--------------------------------------------------------------------
ydl_opts = {
    'format': 'bestaudio/best',
    'postprocessors': [{
        'key': 'FFmpegExtractAudio',
        'preferredcodec': 'mp3',
        'preferredquality': '192',
    }],
}
#--------------------------------------------------------------------

#--------------------------------------------------------------------
# DOWNLOAD SOUNDS
#--------------------------------------------------------------------
files = []
files = get_list ('list.txt')

for sound in files:
    with youtube_dl.YoutubeDL(ydl_opts) as ydl:
        ydl.download([sound])
